          Bahar Aylarında Okunacak 6 Kitap
          Bahar ayları yeni bir başlangıcın da sembolüdür. Doğa gibi yaşamın da canlandığı bu aylarda kültürel gelişiminize katkıda bulunmanız ise kendinize yapabileceğiniz en büyük iyiliklerin başında geliyor. Bahar aylarında okunabilecek 6 kitabı sizler için derledik.
          Ağaca Tüneyen Baron – Italo Calvino
          Çocuklar ve yetişkinler için uygun bir içeriğe sahip Ağaca Tüneyen Baron, Calvino’nun kaleme aldığı kurmaca bir ütopyadır. Kitap, henüz 12 yaşındayken babasının tavırlarından rahatsız olan bir çocuğun tüm ömrünü ağaçlarda geçirme isteğinı anlatmaktadır. Kitabın kahramanı yeryüzüne bir daha ayak basmamaya kararlıdır ve ömrünün kalan günlerini ağaçta geçirmeye başlar. Italo Calvino’nun kaleme aldığı bu önemli eser bahar aylarında başlayacağınız kitap okuma serüveni için iyi bir başlangıç olabilir.
          Gülün Adı – Umberto Eco
          Çok yönlü bir bilim insanı olan Umberto Eco tarafından kaleme alınan Gülün Adı, Hristiyanlık düşüncesini tarihsel boyutlarıyla tartışan bir roman olmakta birlikte ustaca kurulmuş ve sürükleyici bir polisiyedir. Ayrıca çağdaş eserlere uzun ve özgün soluk getiren dünyanın önemli eserleri arasında yer alıyor. Yetişkinler için uygun olan Gülün Adı, bahar aylarında okunabilecek gözde eserler arasında yer alıyor.
          Dönüşüm – Franz Kafka
          Alman edebiyatının önde gelen isimlerinden biri olan Franz Kafka’nın kaleme aldığı Dönüşüm, sanayi sonrası Batı toplumunun içine düştüğü ve yabancılaşmayı konu alan bir sistem eleştirisidir. 20.yüzyılın en önemli eserleri arasında sayılan Dönüşüm’ün baş kahramanı ise bir böceğe dönüşen Gregor Samsa’dır. Kafka bu eserinde sosyolojik süreçleri güçlü metaforlarla anlatmayı başarmıştır. Roman kısaca tam bir Alman işi.
          Denizler Altında Yirmi Bin Fersah – Jules Verne
          Tanzimat döneminde ilk çevirisi yapılan Jules Verne’nin Denizler Altında Yirmi Bin Fersah eseri Kaptan Nemo’nun denizaltı aracıyla yaptığı yolculuğu ve birçok farklı deniz canlısının yer aldığı bir kurmaca olarak öne çıkıyor. Gizemli bir yolculuğa çıkacağınız bu roman hem çocuklar hem de yetişkinler için uygundur.
          Morgue Sokağı Cinayetleri – Edgar Allan Poe
          Bugün okuduğumuz ve izlediğimiz birçok polisiye romanının atası sayılan Morgue Sokağı Cinayetleri, Edgar Allan Poe tarafından kaleme alınan önemli eserlerden biridir. Roman, Paris’teki hayali bir sokak olan Rue Morgue’da işlenen bir cinayeti konu alıyor. Kitap çocuklar ve yetişkinler için okunabilir düzeyde.
